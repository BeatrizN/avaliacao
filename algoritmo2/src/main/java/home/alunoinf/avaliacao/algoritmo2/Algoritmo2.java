package home.alunoinf.avaliacao.algoritmo2;

/**
 * Classe que verifica paridade de um numero.
 */
public class Algoritmo2 {

    /**
     * Verifica paridade de um numero.
     * @param n numero a ser verificado.
     * @return {@code true} se o número fornecido é
     * par e {@code false}, se o numero fornecido for ímpar.
     */
    public static boolean algoritmo2(int n) {
        return n % 2 == 0;
    }
}
